        <p>
          Ethan Freeburg, a McNeil HS graduate, conceived the Math Relay Program during his final semester at McNeil.
          The original Math Relay Program was designed to streamline the speed of the Math Relay event by supplanting the functions of volunteer teachers
          with the power of computers. It was intended to speed up the grading and response processes for the event, allowing team captains to have more time to interact with their teams,
          therefore have a greater effect as a leader to their teams. This powerful tool also opened the opportunity to track team progress and performance, and evaluate the difficulty of
          the tests that were given each year.
        </p>
        <p>
          The first version of the Math Relay Program was introduced in the Spring of 2015.
          It was barely finished on time, riddled with bugs, and was visually unappealing. Therefore, it was less effective in function than what it was originally intended to do,
          and it was more of a proof of concept than a final product, and was retired promptly afterward.
        </p>
        <p>
          Ethan set about to complete a more complete, better-in-every-way product for the following year with the feedback he received from the 2015 Math Relay. He enlisted the help of
          Roland Ip, Kyle Murdoch, and Michael Lam, and together they built the more robust version of the program you see today in time for the 2016 Math Relay. It was redesigned with a focus on user friendliness and is
          packed with new tools to make the Math Relay even better than before.
        </p>
        <p>
          The current control panel features tools that monitors team performance, relative question difficulty, intuitive event configuration, a simple progressive
          event control panel, an answer key editor, and a new leaderboard. The current answer sheet features streamlined user interface, visually appealing graphics,
          and color-coded responses that indicate if a question has been answered correctly, incorrectly, or missed entirely.
        </p>
        <p>
          This version of the Math Relay Program has a rough total of 9,900 lines of code.
